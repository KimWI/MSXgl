#include "reg.h"

//=============================================================================
// MEMORY DATA
//=============================================================================

// Data to store z80 registers
struct Registers g_Registers;

//=============================================================================
// FUNCTIONS
//=============================================================================

//-----------------------------------------------------------------------------
// Call BDOS function after setting registers from g_Registers data structure
void Registers_CallBDOS()
{
	__asm__("push ix");
	Registers_Apply();
	Call(BDOS);
	Registers_Store();
	__asm__("pop ix");
}

